import pandas as pd
import os
from config import PROJECT_ROOT

# --- Configuration ---
CSV_REPORT_PATH = os.path.join(PROJECT_ROOT, "evaluations", "chatgpt_results.csv")
MD_FILE = os.path.join(PROJECT_ROOT, "evaluations", "chatgpt_rendered_results.md")

def create_markdown_viewer():
    print(f"Reading {CSV_REPORT_PATH}...")
    df = pd.read_csv(CSV_REPORT_PATH)
    
    # Convert the 'is_correct' boolean to words for quick visual scanning
    df['is_correct'] = df['is_correct'].apply(lambda x: "Correct" if x else "Incorrect")
    
    # Rename all columns for the display table (e.g. 'problem_id' -> 'Problem Id')
    df.columns = [col.replace('_', ' ').title() for col in df.columns]
    
    print(f"Generating Markdown file...")
    # Convert the DataFrame to a Markdown table
    markdown_table = df.to_markdown(index=False)
    
    # Add a nice header
    markdown_content = f"""# Evaluated Math Results

> **Note:** This file is automatically generated from `chatgpt_results.csv`. 
> All LaTeX equations should render natively in GitHub!

{markdown_table}
"""
    
    with open(MD_FILE, "w", encoding="utf-8") as f:
        f.write(markdown_content)
        
    print(f"Rendered Markdown viewer saved to: {MD_FILE}")

if __name__ == "__main__":
    create_markdown_viewer()
